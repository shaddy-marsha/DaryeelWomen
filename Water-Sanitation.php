         <h4 class="sub-title">Rock Catchments</h4>
         <p class="para">A type of rain water harvesting technique where a large rock outcrop is utilised to catch and concentrate rainwater runoff into a storage structure for domestic and 
          livestock use to alleviate water shortages. This technique is very site specific and since it depends on the natural rock surface for collecting runoff, suitable site selection is critical. 
          It requires sufficient slope, natural clean rock, surface, small retaining/guide walls to direct runoff to a storage tank and above all, very good discipline amongst the community to 
          ensure restricted access by people and animals to the rock surface. Rock water harvesting is a good solution in areas where groundwater is limited.</p><br>

          <h4 class="sub-title">Sub-surface Dams</h4>
         <p class="para">The basic principle of the subsurface dam is that instead of storing the water in surface reservoirs, water is stored underground. 
          The main advantage of water storage in subsurface dam is that evaporation losses are much less and the risk of contamination of the stored water from 
          the surface is reduced as parasites cannot breed in underground water. Pastoralist communities are very familiar with the concept, drawing water for both 
          human consumption and livestock at times when other sources have dried up</p><br>

          <h4 class="sub-title">Shallow wells</h4>
         <p class="para">Shallow wells can be found in areas where the water table is high enough for their construction. 
          They are also relatively cheap to install but can easily be contaminated is not well designed.  
          Scope exists for the development of shallow wells near to the larger storage reservoirs taking advantage of the locally raised water 
          tables and also the natural filtering properties of the soils.</p><br>

          <h4 class="sub-title">Water yards </h4>
         <p class="para">A water yard is an enclosed water supply system constituting borehole equipped with pumping system that supply 
          water from the borehole to elevated water tank, from which water flows freely by gravity to water distribution point, 
          cart filling point and animal troughs. Solar pumping systems are available for use.</p><br>

          <h4 class="sub-title">Roof water harvesting and underground storage for schools </h4>
         <p class="para">Roof water harvesting has been practiced for a long time to supply small quantities of water during the rainy season. 
          Local communities are trained to scale up this practice by installing gutters, down pipes, slow sand filters and 
          large underground tanks in selected schools. This will ensure continuous supply of water for schools during and after the rain season.</p><br>

          <h4 class="sub-title">Improved hygiene and sanitation </h4>
